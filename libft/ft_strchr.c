#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		cpt;

	cpt = 0;
	while (s[cpt] != c && s[cpt] != '\0')
		cpt++;
	if (s[cpt] == '\0' && c != '\0')
		return (NULL);
	return ((char *)&s[cpt]);
}
